package com.paradigmcreatives.audit.beans;

public class Talukas {

	private int taluka_id;
	private int state_id;
	private int district_id;
	private String taluka_name;

	public int getTaluka_id() {
		return taluka_id;
	}

	public void setTaluka_id(int taluka_id) {
		this.taluka_id = taluka_id;
	}

	public int getState_id() {
		return state_id;
	}

	public void setState_id(int state_id) {
		this.state_id = state_id;
	}

	public int getDistrict_id() {
		return district_id;
	}

	public void setDistrict_id(int district_id) {
		this.district_id = district_id;
	}

	public String getTaluka_name() {
		return taluka_name;
	}

	public void setTaluka_name(String taluka_name) {
		this.taluka_name = taluka_name;
	}

}
